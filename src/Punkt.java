
public class Punkt {
	
	private int x;
	private int y;
	private int position;
	
	public Punkt(int x, int y, int position) {
		this.x = x;
		this.y = y;
		this.position = position;
	}
	
	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getPosition() {
		return position;
	}

	public void setPosition(int position) {
		this.position = position;
	}
	
	
	

}
